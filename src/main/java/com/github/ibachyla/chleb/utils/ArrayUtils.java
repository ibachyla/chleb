package com.github.ibachyla.chleb.utils;

import java.util.Arrays;

/**
 * Utility class for various array operations.
 */
public final class ArrayUtils {

  /**
   * Fills the given array with zeroes.
   *
   * @param array the array to fill with zeroes
   */
  public static void fillWithZeroes(char[] array) {
    Arrays.fill(array, '0');
  }
}
